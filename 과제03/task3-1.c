// 과제 03
// signed int 변수를 화면에 출력하는 방법
// printf("%d\n", value)

// unsigned int 변수를 화면에 출력하는 방법
// printf("%u\n", value)

#include <stdio.h>

int main(void) {
	signed int value1 = 10;
	printf("%d\n", value1);

	unsigned int value2 = 10;
	printf("%u\n", value2);

	return 0;
}
